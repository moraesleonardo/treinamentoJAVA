public class Decimals {
    public static void main(String[] args) {
        double percentage = 55.72;
        System.out.println("In 2020, " + percentage + " % of the world's population.");
        double dividend = 25;
        double divisor = 2;
        System.out.println(dividend + " divided by " + divisor + " = " + dividend/divisor);
    }
}
